            <p class="casesPage__content_text">Joom Marketplace helps consumers make shopping easy and entertaining, giving access to high quality goods from around the world. The brand was looking to capitalise on the buying potential of new users through their campaigns, as well as find a new way to reach engaged audiences. For the past years, Joom has been trying different ways but unfortunately all methods weren’t quite effective. The client approached our team in March, 2021 to work out the same challenge and hoped to leverage deep funnel solutions to reignite solvent customers and increase sales. </p>
            <h2 class="casesPage__content_titleMin">Overview </h2>
            <div class="casesPage__content_tools min">
                <div class="cards">
