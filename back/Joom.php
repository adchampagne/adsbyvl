            <h2 class="casesPage__content_titleMin">Our Approach with TikTok Ads</h2>
            <p class="casesPage__content_text">Part of the strategy implemented to achieve this business goal involved using a robust TikTok Ads campaign. We took a series of well-planned steps as outlined below.</p>
            <ul class="casesPage__content_list">
                <li>Audited Joom past campaigns and determined what performed well and what could be improved</li>
                <li>Created TikTok short-form videos specialized for the client and developed a testing strategy</li>
                <li>Launched initial setup including managing of control groups, test balancing, budgets, and confirming testing windows</li>
                <li>Procured all data tracking was in place by checking the conversion tracking and MMP were set up correctly</li>
                <li>Monitored the campaigns daily to get the lowest cost for the best ROAS</li>
                <li>Conducted biweekly meetings with the client to discuss findings and develop next steps and strategies based on the Joom’s new categories and features</li>
            </ul>
            <p class="casesPage__content_text">Due to the close-knit teamwork our team managed to create, adapt and test dozens of videos. We use three main formats with an eye on building trust, raising brand’s credibility, and increasing customers loyalty (as well as different videos for retargeting):</p>
            <ul class="casesPage__content_list">
                <li>Shopping imitations and add-to-cart videos (to motivate to make a purchase)</li>
                <li>WOW-effect during the first 2-3 seconds (to draw customers attention)</li>
                <li>Unboxing (to show products exactly as they are)</li>
            </ul>
            <p class="casesPage__content_text">The test lasted two month and gathered enough users to achieve statistical significance and confirm our hypothesis – Joom’s installs grew by 112 %.</p>
            <h2 class="casesPage__content_titleMin">Results </h2>
            <p class="casesPage__content_text">In just a few months, Joom drove more than 400k installs working with AdChampagne. Combining VBO and App Event Optimisation strategies produced great results with over 14,000 purchases made off the back of the In-Feed Ad campaign.</p>
