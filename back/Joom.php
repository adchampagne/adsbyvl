            <p class="casesPage__content_text">Our main objective was <b>newly launched items sales</b>, with different <b>in-app event optimisation targets</b>, such as installs, add-to-cart buttons or in-app purchases.</p>
            <p class="casesPage__content_text">In partnership with Joom, we ran in-feed auction ads as the central solution, with a core focus of re-engaging audiences. The brand made multiple value based optimisation (VBO) campaigns across various regions, and the ads were optimised towards ROAS. The approach was always-on, cost efficient, and featured creative that felt native to their key audiences who had already shown an affinity for Joom products. Besides, authentic and ownable short videos, optimized for a target consumer, were incredibly funny, and above all, demonstrated product use and raised awareness for different categories (especially, clothing and accessories).</p>
            <h2 class="casesPage__content_titleMin">User Acquisition  </h2>
            <p class="casesPage__content_text">The creatives included strong native TikTok elements such as text overlay, an intro within the first 3 seconds, text-to-speech, and trending sounds from the GML. Our team leveraged these organic videos as Spark ads and ran in-feed campaigns aiming to deliver a strong ROAS while maximizing conversions. We highlighted the most selling products along with the message: «Joom – buy with pleasure!», while targeting predominantly female audiences ages 18-55+. Due to TikTok unique algorithm, we were able to catch the right users with the right products, grabbing their attention and connecting them in a way they hadn’t been able to achieve on other platforms.</p>
            <p class="casesPage__content_text">AdChampagne team localised creatives in ten different languages, which gave an authentic feel to ads and appealed to a wider range of local target audiences. The brand built confidence and trust with consumers by putting a unique twist on their repurposed organic videos. The content demonstrated the type of products available through the Joom website and used TikTok’s in-app editing tools to overlay text and voiceover to highlight the quality of their offerings.</p>
