            <h2 class="case__solution_title">Decision</h2>
            <div class="case__solution_full-desc">
                <div class="case__solution_full-left">
                    <p>NUUM is&nbsp;a&nbsp;neobank and&nbsp;marketplace for&nbsp;gamers from&nbsp;MTS. What is&nbsp;neobank? Not&nbsp;everyone is&nbsp;aware of&nbsp;this term, and&nbsp;bringing such products to&nbsp;the&nbsp;market is&nbsp;an&nbsp;interesting challenge. To&nbsp;begin with, what are&nbsp;the&nbsp;mechanics? The&nbsp;client downloads NUUM&nbsp;app, registers, receives a&nbsp;virtual card and&nbsp;uses&nbsp;it. User also has&nbsp;the&nbsp;opportunity to&nbsp;accumulate a&nbsp;loyalty program point-back and&nbsp;exchange his points: spend them on&nbsp;buying games and&nbsp;add-ons to&nbsp;them, game currency, subscriptions to&nbsp;different services, as&nbsp;well&nbsp;as MTS&nbsp;ecosystem products (Kion online cinema or&nbsp;MTS&nbsp;Premium).</p>
                    <p>What is&nbsp;the&nbsp;story behind neobank's appearance? Although neobanks as&nbsp;a&nbsp;separate business came into&nbsp;view more than&nbsp;six&nbsp;years ago, there was&nbsp;no&nbsp;platform for&nbsp;a&nbsp;gaming segment before. In&nbsp;August&nbsp;2021, NUUM was&nbsp;looking for&nbsp;an&nbsp;agency they could trust, so&nbsp;they chose the&nbsp;AdChampagne team. One of&nbsp;the&nbsp;main requirements for&nbsp;a&nbsp;partner was&nbsp;the&nbsp;experience with&nbsp;TikTok campaigns - AdChampagne had&nbsp;already run into&nbsp;different advertising campaigns with&nbsp;this&nbsp;platform. </p>
                    <p>Nowadays every third case is&nbsp;closely related to&nbsp;TikTok. Over the&nbsp;past three years, this&nbsp;platform has&nbsp;been actively growing, and&nbsp;also the&nbsp;audience, which is&nbsp;expanding in&nbsp;terms of&nbsp;demographics and&nbsp;social changes. If&nbsp;in&nbsp;the beginning TikTok was&nbsp;perceived as&nbsp;a&nbsp;social network for&nbsp;very young users, now, according to&nbsp;TikTok's internal data, you can most often find people between the&nbsp;ages of&nbsp;25 and&nbsp;34 here.</p>
                    <p>Moreover, 90%&nbsp;of&nbsp;them spend an&nbsp;average of&nbsp;52&nbsp;minutes a&nbsp;day on&nbsp;this app. So&nbsp;there will&nbsp;be&nbsp;different users on&nbsp;the&nbsp;platform: from&nbsp;office workers to&nbsp;video game lovers. AdChampagne has&nbsp;already collaborated with&nbsp;the&nbsp;banking segment and&nbsp;has&nbsp;expertise in&nbsp;launching fintech products. That's why the&nbsp;team suggests techniques that allow getting high-quality leads.</p>
