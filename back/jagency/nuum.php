                    <h2 class="video__title">Promotional campaign with&nbsp;Wargaming</h2>
                    <p>We offer new users popular bonus sets and&nbsp;in-game items that could only be&nbsp;purchased through the&nbsp;NUUM&nbsp;app. More than 100&nbsp;different materials were&nbsp;created with&nbsp;an&nbsp;emphasis on&nbsp;cyberpunk elements. New creatives almost immediately began to&nbsp;"go viral" with&nbsp;TikTok algorithms and&nbsp;get into&nbsp;recommendations. Users began to&nbsp;invent their own interpretations and&nbsp;publish collaborative videos.</p>
                    <p>At&nbsp;the&nbsp;end of&nbsp;August&nbsp;2021, the&nbsp;NUUM&nbsp;app made it to&nbsp;the&nbsp;TOP-5 on&nbsp;Google&nbsp;Play. Although young audiences often make decisions about downloading the&nbsp;app impulsively and, subsequently, quickly stop using it, the&nbsp;degree of&nbsp;engagement is&nbsp;still at&nbsp;a&nbsp;high level. This proves that&nbsp;the&nbsp;segments for&nbsp;targeting were&nbsp;chosen correctly.</p>
                    <p>In&nbsp;the&nbsp;process, the&nbsp;client had&nbsp;a&nbsp;desire to&nbsp;test additional traffic sources and&nbsp;attract new users: in&nbsp;September, we decided to&nbsp;connect Google&nbsp;Play, Facebook and&nbsp;myTarget.</p>
                    <h2 class="video__title">Results</h2>
                    <p>From the&nbsp;first contact with&nbsp;the&nbsp;client to&nbsp;the&nbsp;first results, only 14&nbsp;days passed: in&nbsp;August, 10&nbsp;000 new cards were&nbsp;issued in&nbsp;two&nbsp;weeks, and&nbsp;the&nbsp;number of&nbsp;app downloads increased to&nbsp;100&nbsp;000.</p>
