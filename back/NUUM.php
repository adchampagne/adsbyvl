            <h2 class="casesPage__content_titleMin">User Acquisition  </h2>
            <p class="casesPage__content_text">AdChampagne team worked together with NUUM to create the communication strategy to attract and retain the right audience. Based on the brainstorming session, we developed a series of short videos to test and optimize campaigns performance.</p>
            <p class="casesPage__content_text">During strategy development, the AdChampagne team decided to build on the key advantages of the NUUM card:</p>
            <ul class="casesPage__content_list">
                <li>high pointback;</li>
                <li>discounts on items;</li>
                <li>the presence of unique game items that can be purchased inside the application for bonuses.</li>
            </ul>
            <p class="casesPage__content_text">Moreover, some of these items are archived, which was appreciated by gamers themselves. In addition, classical texts were used with a call to action and indication of the final benefit that the user will receive: «Apply for a NUUM card and pick up game items with a discount of up to 50% – save money on the games you play!».</p>
