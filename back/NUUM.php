            <h2 class="casesPage__content_titleMin">Our approach with TikTok Ads  </h2>
            <p class="casesPage__content_text">A/B Testing showed TikTok to be an extraordinarily profitable ad channel. While working with this platform, AdChampagne team came to the conclusion that the typical approach – «just showing a new product attractively» – doesn't work here. Any interaction between the brand and the audience must evoke emotions. Video creations adapted for game audiences have become the most appropriate format to draw users' attention to NUUM: to motivate them to learn what neobank is, and to sign up for the card.</p>
            <p class="casesPage__content_text">Our best creative practices:</p>
            <ul class="casesPage__content_list">
                <li>Analyze brand past campaigns and determined what performed well and what could be improved</li>
                <li>Update creatives at least every 4-5 days to keep your ads fresh </li>
                <li>Video length must be at least 15-30 seconds – shorter videos don’t always perform better </li>
                <li>Try to drive users’ attention during the first 2-3 seconds</li>
                <li>Study up TikTok’s ad policy to reduce creative rejections</li>
            </ul>
            <p class="casesPage__content_text">We offer new users popular bonus sets and in-game items that could only be purchased through the NUUM app. More than 100 different materials were created with an emphasis on cyberpunk elements. New creatives almost immediately «go viral» with TikTok algorithms and get into recommendations. Users began to invent their own interpretations and publish collaborative videos.</p>
            <p class="casesPage__content_text">At the end of August 2021, the NUUM app made it to the TOP-5 on Google Play. Although young audiences often make decisions about downloading the app impulsively and, subsequently, quickly stop using it, the degree of engagement is still at a high level. This proves that the segments for targeting were chosen correctly.</p>
            <p class="casesPage__content_text">During the process, the client had a desire to test additional traffic sources and attract new users: in September, we decided to connect Google Play, Facebook, and myTarget.</p>
            <h2 class="casesPage__content_titleMin">Results </h2>
            <p class="casesPage__content_text">From the first contact with the client to the first results, only 14 days passed: in August, 10,000 new cards were issued in two weeks, and the number of app downloads increased to 100,000.</p>
            <p class="casesPage__content_text">This led NUUM to experience organic downloads up 3821% in Q3 (compared to Q2) and we are already planning our next major campaign.</p>
