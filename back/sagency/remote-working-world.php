            <h1 class="case__title">Work & Life Balance in a Remote Working World</h1>
            <div class="case__solution_full-desc">
                <div class="case__solution_full-left">
                    <p>Previously, the pandemic made distance work a normal practice. Now remote format is changing and progressing as well as what people want from the perspective of teleworking. According to <a target="_blank" href="https://www.theladders.com/press/research-remote-work-now-accounts-for-nearly-15-of-all-high-paying-jobs">Ladders, Inc. research</a>, distance opportunities jumped from under 4% of all high paying jobs before the pandemic to about 9% at the end of 2020, and to more than 15% today. More and more companies are offering remote vacancies to fulfill people’s desire for flexibility at work. There is no doubt that distance work has become a solution as this is one of the most stable niches nowadays. </p>
                    <p>Another benefit of teleworking is the <b>«democratization» of employment</b>: companies are not limited to hiring talent within their own geographic footprint. Therefore, people’s advancement depends on their ability to hardwork and initiativity — not whether they are located. We are convinced that talented people live worldwide and it’s sometimes necessary to search candidates outside its borders. </p>
                    <p>Despite this, job burnout syndrom didn’t go anywhere. Stress from endless workdays is quite disturbing. It can destroy relationships, mental health and overall state of mind. </p>
                </div>
            </div>
            <div class="case__solution_full-desc">
                <div class="case__solution_full-left">
                    <div class="author">
                        <p class="author__desc"><a href="">American Psychological Association’s Work and Well-Being survey</a> found that 79% of the 1,501 employees experienced stress during their workflow. Three in five workers said they can’t handle a lack of interest, motivation and energy at work.</p>
                    </div>
                    <p>Such problems have a strong impact on our home life and work-life balance. You just may be feeling lonely when working alone, ​discouragement or sadness as well. Apart from that, remote workers sometimers think their colleagues are talking negatively about them behind their back and all of these factors can also affect your mood. Looks like it's time to take care of yourself. </p>
                    <h2 class="video__title">Why Does It Matters and How to Achieve It?</h2>
                    <p><i>One of the biggest discoveries of remote work is greater flexibility. With the opportunity to work remotely, employees can take advantage of this by performing most of the functions from home and creating a flexible timetable to take care of working activities and their own responsibilities.</i></p>
                    <p><i>Although, this flexibility sometimes comes with different psychological effects. If you're working non-traditional schedules, or if you're not getting in face-time with your partners every day, it might be more difficult to keep your productivity. Besides, without teammates around to remind you to take a rest, have lunch, and leave the office for the day, remote employees might have a serious pressure and overworking. </i></p>
                    <h2 class="video__title">So… How to Balance Work & Life While Working Remotely? <br><br>Tips from AdChampagne:</h2>
                    <img class="video__img" src="local/assets/images/blog1-1.png" alt="">
                    <p>We are not talking about your «private office», but your back will definitely thank you just for investing in a comfortable chair and desk. Lying down on bed or on the couch is also not the best idea as this is a relaxation area. Distance working provides an opportunity to organize a space that fuels your performance: get favorite art, pictures or music playing.  </p>
                    <img class="video__img" src="local/assets/images/blog1-2.png" alt="">
                    <p>It is better to forget about all distracting apps while you work (use Focus Lock or AppBlock for this). You can just сhoose those sites or apps that interfere with concentration and turn them off for a certain time to focus on more important things. If you don’t want to download another app, check your own emails only 2 times a day – in the morning and in the evening. </p>
                    <img class="video__img" src="local/assets/images/blog1-3.png" alt="">
                    <p>Having other activities outside of workday is also important. Whether the plans include meeting some friends, doing yoga or meditations, make a plan to help balance your work tiredness. </p>
                    <div class="author">
                        <div class="author__title">
                            <img style="display: none;" src="local/assets/images/author__img.png" alt="" class="author__title_img">
                            <div class="author__title_name">
                                Ekaterina Rudnichenko <br>
                                <span>Head of Sales AdChampagne</span>
                            </div>
                        </div>
                        <p class="author__desc">Remote format gives great freedom of movement: working from other time zones becomes possible. Although, it’s still important to properly allocate time for personal life and balance in general. For one period I was in the Asian area and working hours fell into the afternoon and evening. Then it was more appropriate to have my own time in the morning – I had an opportunity to surf, see friends or go to a cafe.</p>
                    </div>
                    <img class="video__img" src="local/assets/images/blog1-4.png" alt="">
                    <p>The communication needs of distance teams are always different. But there is no doubt that building a meaningful rapport with co-workers is a separate block that sometimes doesn’t remain unnoticed. Ask your colleagues what they’re doing this evening, make a quick chat and take an opportunity to get to know them more. </p>
                    <p>We can observe that the percentage of people working remotely is only expected to increase. And your productivity may not suffer in any way. Now distance work is a little bit more than just a temporary phenomenon. It's here to stay.</p>
                    <p><i>Want more tips on how to stay balanced in the remote working world? Head over to AdChampagne blog articles!</i></p>
