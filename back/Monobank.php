            <p class="casesPage__content_text">Our team suggests TikTok as the main channel for client’s promotion, as it’s the perfect way for testing new features and placements. Besides, TikTok has a massive audience of twenty and thirty-somethings, and the finance topics are strongly related to it. </p>
            <h2 class="casesPage__content_titleMin">User Acquisition  </h2>
