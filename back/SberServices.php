            <h1 class="casesPage__content_title">Case Study: SberServices – Self-Ordering Platform </h1>
            <h2 class="casesPage__content_titleMin">Background </h2>
            <p class="casesPage__content_text">SberServices launched its new service ordering platform at the end of 2020, with the aim of helping self-employed workers to find jobs. Our team needs to increase the number of first registrations of clients and customers from various categories, as well as maintain a balance between them. </p>
            <h2 class="casesPage__content_titleMin">Overview </h2>
            <div class="casesPage__content_tools min">
