            <h2 class="casesPage__content_titleMin">Our approach with TikTok ads</h2>
            <p class="casesPage__content_text">TikTok proved to be a winning channel after testing budgets to try out the main idea. At AdChampagne, we implement an iterative approach to creative assets that help SberServices quickly identify winning ideas and scale performance:</p>
            <ul class="casesPage__content_list">
                <li>Our sales team worked closely with SberServices from day one, performing an in-depth app audit that focused on identifying points of growth, while implementing a series of targeted actions across several activities (myTarget, VK, TikTok) to deliver great results.  </li>
                <li>With a dedicated team of creative and brand experts, we supported SberServices redefining their communication approach, putting storytelling mechaniques at the center and speaking their language to increase relevancy and improve results.</li>
                <li>Our paid user acquisition team identified TikTok as a great opportunity to scale at lower costs. We tested more than 100+ creatives to avoid ad fatigue on TikTok and deliver highly targeted UGC-content.</li>
            </ul>
            <h2 class="casesPage__content_titleMin">Results </h2>
            <p class="casesPage__content_text">The results achieved on TikTok and other social media platforms were impressive, and our Paid UA team was able to scale the marketing budget, while keeping CPA within target.</p>
            <p class="casesPage__content_text">AdChampagne in-house creative studio constantly analyzed creative performances to iteratively test different ideas and solutions. UGC Ads were used on Paid Social campaigns (myTarget, VK, TikTok) allowing us to reach stunning results over a longer period of time.</p>
