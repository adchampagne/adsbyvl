                    </div>
                </div>
            </div>
            <p class="casesPage__content_text">We know how to correctly match the audience of the traffic source with the ad creative that would be catchy for them. Such an approach allows us to produce various performing ads and reach out to the feelings of potential users. Then, our team decided to build on the available service categories, because each of them needs a different approach: some of the users are looking for a pet care specialist, while someone is planning to clean the wardrobe and looking for a personal stylist.</p>
            <h2 class="casesPage__content_titleMin">User Acquisition  </h2>
            <p class="casesPage__content_text">A solid and clear creative strategy that focuses on communicating the right advantages to the target audiences is key for long-term success. We performed over a 4-week period an in-depth mobile app audit to identify growth levers, as well as improving the app offering with separate regions. Why? User habits are also changing, people beginning to delegate more and more chores in order to free time for personal affairs.</p>
            <p class="casesPage__content_text">Our main goal was to tell users as much as possible about the new product, which allows to find a suitable solution for your problem just «in one touch». Together with SberServices we tested lots of creatives with various storylines. Precisely, a storytelling approach helped us to create interest and make an audience receptive to our message. We played out traditional life situations that almost everyone has ever faced. Besides, users strongly reacted to topics related to «Beauty & Health» and «Pet Care». Therefore, we decided to focus primarily on them.</p>
            <p class="casesPage__content_text"><i>Example: Imagine you had a party in the apartments, but there is no time for spring-cleaning. So you need to find an experienced specialist who will help you to solve this problem</i></p>
            <p class="casesPage__content_text">Then, our team concentrated on two main audience segments:</p>
            <ul class="casesPage__content_list">
                <li>Those, who are in search for clients;</li>
                <li>Those, who are in search for customers.</li>
            </ul>
            <p class="casesPage__content_text">That’s why the key message was varied: for the first segment we emphasized that all clients can now be easily found here, and for the second one – that the platform suggests reliable specialists with reviews from real customers.</p>
