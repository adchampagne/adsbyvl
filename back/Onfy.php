                        <span>Germany  </span>
                    </div>
                    <div class="card">
                        <p>Platforms</p>
                        <span>iOS, Android </span>
                    </div>
                    <div class="card">
                        <p>KPI</p>
                        <span>Daily purchases  </span>
                    </div>
                    <div class="card">
                        <p>Model</p>
                        <span>RevShare </span>
                    </div>
                </div>
            </div>
            <p class="casesPage__content_text">Our main objective was to increase daily product sales using various In-App Event Optimisation Targets, such as Installs, Add-to-Cart buttons, or In-App Purchases.</p>
            <p class="casesPage__content_text">AdChampagne was given the goal to forge the brand’s ties to a wide, solvent audience, as well as build brand awareness and relevancy among 18-25-year-olds with maximum cost efficiency.</p>
            <p class="casesPage__content_text">The campaign had to highlight key product features, including Onfy’s advantage of prices being 20-80% lower than in retail.</p>
            <p class="casesPage__content_text">For Onfy’s promotion, we suggested two main channels: TikTok and Facebook. TikTok facilitated the largest number of purchases, while Facebook hyperlocal targeting managed to attract more solvent users. An essential part of the Facebook campaign was also developing different video ad creatives to engage the target healthcare market.</p>
            <h2 class="casesPage__content_titleMin">User Acquisition  </h2>
            <p class="casesPage__content_text">Our general creative idea was to show the strongest parts of the product as evidently as possible. We conducted overall research using the Sensor Tower and realized that the Online Pharmacy market in Germany is one of the most mature in the world. For example, Revenue in this segment will reach US$0.80bn in 2022. Onfy entered the competitive sphere, however, its unique positioning imposes additional benefits for traffic attraction.</p>
            <p class="casesPage__content_text">For 2 weeks, A/B testing was carried out to identify the highest-performing approaches. We experimented with colour, pictures, soundtrack, CTA, and graphics. Afterward, our team developed over 100 ad creatives with User-Generated Content and AdChampagne used three core ideas at the start of the campaign: Unpackings, Shopping Imitations, Comedy Sketches. We also provide custom videos with graphics: our creators let their creativity shine while utilizing Onfy’s mascots and brand colours to stand out from the other content.</p>
