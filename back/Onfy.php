            <h1 class="casesPage__content_title">Case Study: Onfy Pharmaceutical Marketplace</h1>
            <h2 class="casesPage__content_titleMin">Onfy’s User-Generated Content Approach Drives Online Pharmacy Sales </h2>
            <h2 class="casesPage__content_titleMin">Background </h2>
            <div class="casesPage__content_tools">
                <div class="cards">
                    <div class="card">
                        <span>Top-1 Pharmacy App in Germany </span>
                    </div>
                </div>
