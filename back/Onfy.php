            <p class="casesPage__content_text">Onfy is a pharmacy marketplace that provides access to over 50,000 medicines and wellness products including cosmetics, vitamins, and sports nutrition. Purchases can be made on the website <a href="https://onfy.de/" target="_blank">https://onfy.de/</a> or through the Onfy mobile app. It allows users to track their order status and receive cashback on each purchase.</p>
            <p class="casesPage__content_text">The team knew that finding the right approach to getting mobile was crucial to their strategy. The range of rapidly expanding platforms with a growing number of users made the choice of approach even more complex. Nevertheless, when Onfy saw a great opportunity to connect with young, active customers through a new platform, the company jumped at it.</p>
            <h2 class="casesPage__content_titleMin">Overview </h2>
            <div class="casesPage__content_tools min">
                <div class="cards">
                    <div class="card">
                        <p>Sources</p>
                        <span>TikTok, Facebook </span>
                    </div>
