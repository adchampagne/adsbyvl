            <p class="casesPage__content_text">Comedy Sketch is one of the high-converting formats, especially for E-Commerce brands, – it lets users imagine various life-like circumstances where they would need specific goods. This type of video looks organic and facilitates an authentic dynamic between the brand and the viewer.</p>
            <h2 class="casesPage__content_titleMin">Our Approach with TikTok Ads</h2>
            <p class="casesPage__content_text">Taking the lead on Onfy’s performance strategy, we divided our activities into the following stages:</p>
            <ul class="casesPage__content_list">
                <li>Created development media plan including full audit analyzing and identifying Onfy’s target audience demographics as well as competitors in the pharmaceutical space </li>
                <li>Campaign building and content creation, drafts and mocked-up creatives with clear CTAs for client review and approval. We use high-quality, native formats, as well as testimonials and UGC-content </li>
            </ul>
            <p class="casesPage__content_text">Our powerful strategy and execution helped Onfy to majorly boost their return on ad spend and drive a wide expansion on their daily purchases. AdChampagne team also partnered with Adjust to measure every touchpoint in the customer journey and capture data across all its marketing channels. </p>
            <h2 class="casesPage__content_titleMin">Results </h2>
            <p class="casesPage__content_text">To achieve the KPIs, we provided the team of buyers with relevant training, assigned several specialists to the project, and continued to develop and test various hypotheses.</p>
            <p class="casesPage__content_text">The overall results were far from average. During the first month, we achieved over 30,000 installs and 2,000 purchases. While app performance continues to progress, customer engagement remains strong. Revenue is growing due to paid users who generated an increasing share of the overall income. Onfy was ranked first in the Pharmacy app Category in Germany within three weeks of launch.</p>
